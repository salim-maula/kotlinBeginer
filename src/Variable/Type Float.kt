package Variable

/**
 * ciri variabel float memiliki huruf f di belakang angka
 * variabel type float dan double adalah variable dengan nila desimal
 * */

fun main() {
    val uangsaku:Float
    uangsaku = 30.0f

    val uangjajan:Double
    uangjajan=30.0

    println("uangsaku = $uangsaku dan uang jajan = $uangjajan" )
}