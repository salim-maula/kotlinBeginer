fun main() {
    /**
     * apa itu conditional operator
     * mkondisional operator memiliki nilai pengembalian boolean
     * <
     * >
     * ==
     * !=
     * <=
     * >=
     * */

    val nilaipertama = 10
    val nilaikedua = 8

    println(nilaipertama < nilaikedua)
    println(nilaipertama > nilaikedua)
    println(nilaipertama == nilaikedua)
    println(nilaipertama != nilaikedua)
    println(nilaipertama <= nilaikedua)
    println(nilaipertama >= nilaikedua)
}