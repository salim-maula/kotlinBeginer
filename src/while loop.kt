fun main() {
    /**
     * While loop (perulanhan)
     * incrementing dan decrement
     * */

     var perhitungan = 11
    while (perhitungan >= 5 ){
        println("perhitungan....$perhitungan")

        perhitungan -- //pengurangan -1 pada value pada variable dalam perhitungan
    }
    println("nilai hitung out of range")
}