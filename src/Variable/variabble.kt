package Variable

fun main(args: Array<String>) {
    /**
     * perbedaan antara val dan var
     * kesimpulanya val adalah cara mendeklarasikan variable yang mutabel (tetap/konstan/tidak bisa berubah
     * sedangkan var adalah sebaliknya*/

// deklarasi variable dengan var
    val lastName = "muhammad"


    val nama: String = "salim maula"


    val alamat = "Indonesia"


    print("nama saya adalah $lastName $nama dan alamat saya di $alamat")
}
